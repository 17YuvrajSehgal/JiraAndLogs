# FEATURE_COLUMNS is the source of truth for which numeric columns the model
# trains on. It is REPLACED at the top of run_benchmark() with whatever the
# global dataset's triage-feature-columns.json declares — that file is itself
# regenerated by build_global_triage_dataset.py via dynamic key discovery, so
# v5's new RED / business / runtime feature columns flow through end-to-end
# without code changes. The import above is the v4-era fallback used only
# when the contract file is missing.
FEATURE_COLUMNS: tuple[str, ...] = _DEFAULT_FEATURE_COLUMNS


def _load_feature_columns(global_dir: Path) -> tuple[str, ...]:
    """Read the production-safe feature list from triage-feature-columns.json.

    Falls back to the hardcoded v4 default when the file is missing (e.g. an
    older dataset). Warns when the contract file declares columns we cannot
    find in the row data, but does NOT remove them — downstream code zero-fills
    via row.get(col, 0.0), which is the documented behavior."""
    contract_path = global_dir / "triage-feature-columns.json"
    if not contract_path.exists():
        print(
            f"WARN: {contract_path} not found; falling back to hardcoded "
            f"{len(_DEFAULT_FEATURE_COLUMNS)}-column v4 feature catalog",
            file=sys.stderr,
        )
        return _DEFAULT_FEATURE_COLUMNS
    with contract_path.open(encoding="utf-8-sig") as handle:
        contract = json.load(handle)
    cols = contract.get("feature_columns") or []
    if not cols:
        print(
            f"WARN: {contract_path} has empty feature_columns; falling back "
            f"to hardcoded {len(_DEFAULT_FEATURE_COLUMNS)}-column v4 catalog",
            file=sys.stderr,
        )
        return _DEFAULT_FEATURE_COLUMNS
    return tuple(cols)

